# Write a python script to use NOT IN operator to display the data not present in list

# Define a list of numbers
numbers = [1, 2, 3, 4, 5]

# Check if the number 3 is not present in the list
if 3 not in numbers:
    print("The number 3 is not present in the list.")

# Check if the number 6 is not pre ssent in the list
if 6 not in numbers:
    print("The number 6 is not present in the list.")
else:
    print("The number 6 is present in the list.")
